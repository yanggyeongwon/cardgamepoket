package portfolio;

public class CardDTO {
	private int rank_tab;
	private String name_tab;
	private int ts_tab;
	private int tm_tab;
	private String modes;
	
	
	public String getModes() {
		return modes;
	}
	public void setModes(String modes) {
		this.modes = modes;
	}
	public int getRank_tab() {
		return rank_tab;
	}
	public void setRank_tab(int rank_tab) {
		this.rank_tab = rank_tab;
	}
	public String getName_tab() {
		return name_tab;
	}
	public void setName_tab(String name_tab) {
		this.name_tab = name_tab;
	}
	public int getTs_tab() {
		return ts_tab;
	}
	public void setTs_tab(int ts_tab) {
		this.ts_tab = ts_tab;
	}
	public int getTm_tab() {
		return tm_tab;
	}
	public void setTm_tab(int tm_tab) {
		this.tm_tab = tm_tab;
	}

	
}
